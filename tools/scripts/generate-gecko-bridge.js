#!/usr/bin/env node
// Minifies packages/webview-x-gecko/www-src/bridge.gecko.js and writes
// the result as a string constant to packages/webview-x-gecko/gecko-bridge-loader.ts.
//
// Usage: node tools/scripts/generate-gecko-bridge.js

const { buildSync } = require('esbuild');
const fs = require('fs');
const path = require('path');

const srcPath = path.join(__dirname, '../../packages/webview-x-gecko/www-src/bridge.gecko.js');
const outPath = path.join(__dirname, '../../packages/webview-x-gecko/gecko-bridge-loader.ts');

const result = buildSync({
  entryPoints: [srcPath],
  bundle: false,
  minify: true,
  write: false,
  platform: 'browser',
});

const minified = Buffer.from(result.outputFiles[0].contents).toString('utf8').trim();

const output = `// AUTO-GENERATED by tools/scripts/generate-gecko-bridge.js - do not edit directly.
// Source: packages/webview-x-gecko/www-src/bridge.gecko.js
export const geckoBridgeJs = ${JSON.stringify(minified)};
`;

fs.writeFileSync(outPath, output, 'utf8');
console.log('✓ wrote', path.relative(process.cwd(), outPath));
